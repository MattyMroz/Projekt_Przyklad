    // dzielenie linii
    private void jCheckBox1ActionPerformed(java.awt.event.ActionEvent evt) {
        if (linebreak == false) {
            linebreak = true;
            jTextArea1.setLineWrap(true);
        } else {
            linebreak = false;
            jTextArea1.setLineWrap(false);
        }
    }

    // kopiuje zaznaczony tekst
    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {
        if (jTextArea1.getSelectedText() != null) {
            StringSelection stringSelection = new StringSelection(jTextArea1.getSelectedText());
            Clipboard clipboard = Toolkit.getDefaultToolkit().getSystemClipboard();
            clipboard.setContents(stringSelection, null);
        }

    }

    // wycina zaznaczony tekst
    private void jButton4MouseClicked(java.awt.event.MouseEvent evt) {
        if (jTextArea1.getSelectedText() != null) {
            String text = jTextArea1.getSelectedText();
            StringSelection stringSelection = new StringSelection(text);
            Clipboard clipboard = Toolkit.getDefaultToolkit().getSystemClipboard();
            clipboard.setContents(stringSelection, null);
            jTextArea1.replaceSelection("");
        }

    }

    // wkleja zaznaczony tekst
    private void jButton5MouseClicked(java.awt.event.MouseEvent evt) {
        Clipboard clipboard = Toolkit.getDefaultToolkit().getSystemClipboard();
        Transferable contents = clipboard.getContents(null);
        boolean hasTransferableText = (contents != null) && contents.isDataFlavorSupported(DataFlavor.stringFlavor);

        if (hasTransferableText) {
            try {
                String result = (String) contents.getTransferData(DataFlavor.stringFlavor);
                jTextArea1.replaceSelection(result);
            } catch (UnsupportedFlavorException ex) {
                // highly unlikely since we are using a standard DataFlavor
                System.out.println(ex);
                ex.printStackTrace();
            } catch (IOException ex) {
                System.out.println(ex);
                ex.printStackTrace();
            }
        }
    }


    private void jMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {
        // zapis do pliku
        JFileChooser fc = new JFileChooser();
        // otwóz wybór katalogu i zapisz do nowego pliku
        if (fc.showSaveDialog(this) == JFileChooser.APPROVE_OPTION) {
            File file = fc.getSelectedFile();
            try {
                FileWriter fw = new FileWriter(file);
                fw.write(jTextArea1.getText());
                fw.close();
            } catch (IOException ex) {
                // Logger.getLogger(MainFrame.class.getName()).
                // log(Level.SEVERE, null, ex);
            }
        }

    }

