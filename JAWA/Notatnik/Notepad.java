    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {
        JFileChooser fc = new JFileChooser();
        fc.showOpenDialog(this);
        File plik = fc.getSelectedFile();
        try {
            Scanner sc = new Scanner(plik);
            while (sc.hasNextLine()) {
                String linia = sc.nextLine();
                jTextArea1.append(linia + " ");
            }
            sc.close();
        } catch (FileNotFoundException ex) {
            // Logger.getLogger(Notepad.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    Integer fontsChange = 0;
    String[] fonts = { "Arial", "Times New Roman", "Courier New", "Verdana", "Tahoma", "Calibri", "Comic Sans MS",
            "Impact", "Lucida Console", "Lucida Sans Unicode", "MS Sans Serif", "MS Serif" };
    Integer fontSize = 0;
    Color fontColor = Color.BLACK;
    Color backColor = Color.WHITE;
    Boolean bold = false;
    Boolean italic = false;
    Boolean linebreak = false;

    // zmiana czcionki
    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {
        fontsChange = jComboBox1.getSelectedIndex();
        if (bold == true && italic == true) {
            jTextArea1.setFont(new Font(fonts[fontsChange], Font.BOLD | Font.ITALIC, fontSize));
        } else if (bold == true && italic == false) {
            jTextArea1.setFont(new Font(fonts[fontsChange], Font.BOLD, fontSize));
        } else if (bold == false && italic == true) {
            jTextArea1.setFont(new Font(fonts[fontsChange], Font.ITALIC, fontSize));
        } else {
            jTextArea1.setFont(new Font(fonts[fontsChange], Font.PLAIN, fontSize));
        }
    }

    // zmiana rozmiaru czcionki przy kliknięciu w textArea
    private void jTextArea1MouseClicked(java.awt.event.MouseEvent evt) {
        if (fontSize == 0) {
            fontSize = 11;
            jTextArea1.setFont(new Font(fonts[fontsChange], Font.PLAIN, fontSize));
        }
    }

    // zmiana koloru czcionki
    private void jComboBox2ActionPerformed(java.awt.event.ActionEvent evt) {
        fontSize = Integer.parseInt(jComboBox2.getSelectedItem().toString());
        jTextArea1.setFont(new Font(fonts[fontsChange], Font.PLAIN, fontSize));

    }

    // zmiana na bold
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {
        // bold
        if (bold == false) {
            if (italic == true) {
                bold = true;
                jTextArea1.setFont(new Font(fonts[fontsChange], Font.BOLD + Font.ITALIC, fontSize));
            } else {
                bold = true;
                jTextArea1.setFont(new Font(fonts[fontsChange], Font.BOLD, fontSize));
            }
        } else {
            if (italic == true) {
                bold = false;
                jTextArea1.setFont(new Font(fonts[fontsChange], Font.ITALIC, fontSize));
            } else {
                bold = false;
                jTextArea1.setFont(new Font(fonts[fontsChange], Font.PLAIN, fontSize));
            }
        }
    }

    // zmiana na italic
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {
        // italic
        if (italic == false) {
            if (bold == true) {
                italic = true;
                jTextArea1.setFont(new Font(fonts[fontsChange], Font.BOLD + Font.ITALIC, fontSize));
            } else {
                italic = true;
                jTextArea1.setFont(new Font(fonts[fontsChange], Font.ITALIC, fontSize));
            }
        } else {
            if (bold == true) {
                italic = false;
                jTextArea1.setFont(new Font(fonts[fontsChange], Font.BOLD, fontSize));
            } else {
                italic = false;
                jTextArea1.setFont(new Font(fonts[fontsChange], Font.PLAIN, fontSize));
            }
        }
    }

